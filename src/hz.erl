-module(hz).

%% API
-export([start/1]).

-define(HZ_JAVA, "java -jar ../target/hz.jar").

%%%===================================================================
%%% API
%%%===================================================================


start(Hz_nodes) ->
	Cmd = ?HZ_JAVA ++ " -Dargs " ++ Hz_nodes,
	io:format("CMD: ~p~n", [Cmd]),
	Port = open_port({spawn, Cmd}, [binary, exit_status, eof]),
	Port.

